#!/usr/bin/env python3
"""
Stack Overflow Survey Data Normalizer v2.0
"""

import argparse, csv, json, os, sys
from datetime import datetime
from pathlib import Path

WORKSPACE = Path(__file__).parent.parent.resolve()


def normalize_csv(input_path, output_dir):
    input_file = WORKSPACE / input_path
    output_directory = WORKSPACE / output_dir
    output_directory.mkdir(parents=True, exist_ok=True)
    if not input_file.exists():
        print(f"[ERROR] Input file not found: {input_file}")
        return False
    rows = []
    with open(input_file, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        headers = reader.fieldnames
        for row in reader:
            rows.append({k: v.strip() for k, v in row.items()})
    output_file = output_directory / "normalized_survey.csv"
    with open(output_file, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=headers)
        writer.writeheader()
        writer.writerows(rows)
    summary = {"pipeline": "so-survey-v2", "version": "2.0", "records_processed": len(rows), "columns": headers, "output_path": str(output_file.relative_to(WORKSPACE)), "runtime": datetime.now().isoformat()}
    summary_file = output_directory / "summary.json"
    with open(summary_file, "w") as f:
        json.dump(summary, f, indent=2)
    print(f"[OK] Normalized {len(rows)} records")
    return True


