    parser = argparse.ArgumentParser(description="SO Survey Normalizer v2")
    parser.add_argument("--input", default="data/input.csv")
    parser.add_argument("--output", default="outputs/")
    parser.add_argument("--write-dependency", action="store_true")
    args = parser.parse_args()

